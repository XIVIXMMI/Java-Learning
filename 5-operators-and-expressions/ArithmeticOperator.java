public class ArithmeticOperator {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {

        // float a = 14.3f, b = 3.2f;
        //
        // float r = a%b;
        //
        // System.out.print(r);

        byte a = 10;

        short b = 15;

        int c = a + b;

        System.out.println(c);

        float x = 12.5f;

        long y = 123l;

        float z = x + y;

        System.out.println(z);

        char ch = 40;

        int i = 30;

        int sum = ch - i;

        System.out.println(sum);

    }

}
